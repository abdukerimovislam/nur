enum QuranPracticeDifficulty {
  beginner,
  intermediate,
  advanced,
}

extension QuranPracticeDifficultyX on QuranPracticeDifficulty {
  String get label {
    switch (this) {
      case QuranPracticeDifficulty.beginner:
        return 'Beginner';
      case QuranPracticeDifficulty.intermediate:
        return 'Intermediate';
      case QuranPracticeDifficulty.advanced:
        return 'Advanced';
    }
  }
}

class QuranSurahPractice {
  const QuranSurahPractice({
    required this.number,
    required this.englishName,
    required this.transliteration,
    required this.meaning,
    required this.difficulty,
    required this.ayahs,
  });

  final int number;
  final String englishName;
  final String transliteration;
  final String meaning;
  final QuranPracticeDifficulty difficulty;
  final List<QuranAyahPractice> ayahs;
}

